            $('.gallery-next').click(function() {
                $('.gallery-carousel').trigger('next.owl.carousel');
            });
            
            // Initialize other carousels if they exist
            if ($('.owl-carousel').length > 1) {
                $('.owl-carousel:not(.gallery-carousel)').owlCarousel({
                    loop: true,
                    margin: 20,
                    nav: false,
                    dots: false,
                    responsive:{
                        0:{
                            items:1
                        },
                        768:{
                            items:2
                        },
                        992:{
                            items:3
                        }
                    }
                });
                
                // Custom navigation for other carousels
                $('.custom-prev').click(function() {
                    $(this).closest('.container').find('.owl-carousel').trigger('prev.owl.carousel');
                });
                
                $('.custom-next').click(function() {
                    $(this).closest('.container').find('.owl-carousel').trigger('next.owl.carousel');
                });
            }
